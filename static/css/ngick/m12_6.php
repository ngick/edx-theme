<b>ΠΡΟΒΛΗΜΑ:</b> Ένα ηλεκτρόνιο είναι εγκλωβισμένο σε ένα μονοδιάστατο σωληνάκι μήκους $L=5\,\unicode[serif]{xC5}$.
Υπολογίστε τις επιτρεπόμενες ενέργειές του σε ηλεκτρονιοβόλτ.

<i>Υπόδειξη</i>: Για να βρείτε το αποτέλεσμα κατευθείαν σε eV χρησιμοποιήστε, από τη θεωρία του Bohr, τη σχέση
$$\frac{\hbar^2}{2ma_0^2}=\frac{\hbar^2}{2m(\hbar^2/me^2)}=\frac{me^4}{2\hbar^2}=13,\!6\,\hbox{eV}$$

<b><i>Λύση:</i></b> Αρκεί να υπολογίσουμε την ενέργεια $E_1=\hbar^2\pi^2/2mL^2$ της θεμελιώδους στάθμης αφού όλες οι άλλες θα δίνονται από τον τύπο $E_n=n^2E_1$, $n=1,2,\ldots,\infty$. 
Για το $E_1$ θα έχουμε
$$E_1=\frac{\hbar^2\pi^2}{2mL^2}\equiv \frac{\hbar^2\pi^2}{2ma^2_0}\left. \left(\frac{a_0}{L}\right)^2\right|_{L=10\,a_0}\!\! =\!13,\!6\cdot\pi^2\left(\frac{1}{10}\right)^2\simeq 1,\!36\,\hbox{eV}$$
οπότε ο γενικός τύπος για τις ενεργειακές ιδιοτιμές θα είναι ο

$$E_n=1,\!36 n^2\,\hbox{eV}\qquad (n=1,2,\ldots,\infty)$$
