θέτοντας $p=h/\lambda$ στον τύπο $E=p^2/2m$ και αντικαθιστώντας τις κβαντωμένες τιμές τού $\lambda$ βάσει της συνθήκης δημιουργίας στάσιμων κυμάτων μέσα στον σωλήνα.
