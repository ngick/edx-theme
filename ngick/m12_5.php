$$
E={p^2\over 2m}= \left. \frac{(h/\lambda)^2}{2m}\right|_{{\!\!{\scriptstyle h=2\pi\hbar} \atop {\scriptstyle \lambda =2L/n}}} = \frac{\hbar^2\pi^2}{2mL^2}n^2
